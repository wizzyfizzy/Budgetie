import SwiftUI

public enum KeyboardHelper {
    
    /// Dismisses the keyboard if any text field is active.
    public static func dismiss() {
        UIApplication.shared.sendAction(
            #selector(UIResponder.resignFirstResponder),
            to: nil,
            from: nil,
            for: nil
        )
    }
}
